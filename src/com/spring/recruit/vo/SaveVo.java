package com.spring.recruit.vo;

public class SaveVo {
	private CareerVo careerVo;
	private RecruitVo recruitVo;
	private EducationVo educationVo;
	private CertificateVo certificateVo;
	public CareerVo getCareerVo() {
		return careerVo;
	}
	public void setCareerVo(CareerVo careerVo) {
		this.careerVo = careerVo;
	}
	public RecruitVo getRecruitVo() {
		return recruitVo;
	}
	public void setRecruitVo(RecruitVo recruitVo) {
		this.recruitVo = recruitVo;
	}
	public EducationVo getEducationVo() {
		return educationVo;
	}
	public void setEducationVo(EducationVo educationVo) {
		this.educationVo = educationVo;
	}
	public CertificateVo getCertificateVo() {
		return certificateVo;
	}
	public void setCertificateVo(CertificateVo certificateVo) {
		this.certificateVo = certificateVo;
	}
	
	
}
